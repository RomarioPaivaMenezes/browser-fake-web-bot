package sauter.unitBacnet;

public class BACnetUnits {
	
	private String name;
	private String symbol;
	private String bacNetEngUnit;
	private String precision;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public String getBacNetEngUnit() {
		return bacNetEngUnit;
	}
	public void setBacNetEngUnit(String bacNetEngUnit) {
		this.bacNetEngUnit = bacNetEngUnit;
	}
	public String getPrecision() {
		return precision;
	}
	public void setPrecision(String precision) {
		this.precision = precision;
	}
	
	
	
	
}
